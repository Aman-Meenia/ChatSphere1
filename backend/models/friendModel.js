import mongoose from "mongoose";
import User from "./userModel.js";

const friendSchema = mongoose.Schema(
  {
    sender: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    receiver: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  {
    timestamps: true,
  },
);

const Friend = mongoose.model("Friend", friendSchema);
export default Friend;
